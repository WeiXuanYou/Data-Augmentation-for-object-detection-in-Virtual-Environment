def unreal2yolo(class_name,xmin,ymin,xmax,ymax):
    
    x = (xmin + xmax) /(2*args.img_size[0])
    y = (ymin + ymax) /(2*args.img_size[1])
    w = (xmax-xmin) / args.img_size[0]
    h = (ymax-ymin) / args.img_size[1]
    
    return CLASSES.index(class_name.lower()),x,y,w,h